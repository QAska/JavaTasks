/*
Напишите программу вычисления суммы четырёх слагаемых.
*/

public class SumOfFour {
    public static void main(String[] args) {
        double a;
        double b;
        double c;
        double d;
        double sum;

        a = 4.6;
        b = 3.4;
        c = 5;
        d = 2.1;

        sum = a + b + c + d;

        System.out.println(sum);
    }
}
